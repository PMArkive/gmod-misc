-- Deduplicate materials
hook.Add("PostGamemodeLoaded", "DeduplicateMaterials", function()
    local _ourMaterials = table.Copy(ourMaterials)
    ourMaterials = table.GetKeys(ourMaterials)
    Msg("[Stools] ") print("Previous material count:", prevCount)
    Msg("[Stools] ") print(Format("Adding %d materials...", #ourMaterials))

    local matList = list.GetForEdit("OverrideMaterials")
    local toReplace = {}

    Msg("[Stools] ") print("Condensing down materials...")
    Msg("[Stools] ") print("Materials before:", #matList)
    Msg("[Stools] ") print("Deduplicating...")

    for i, mat in ipairs(matList) do
        local sanitized = sanitize(mat)

        if toReplace[sanitized] then
            matList[i] = nil
            continue
        end

        toReplace[sanitized] = true
        matList[i] = nil
    end

    toReplace = table.GetKeys(toReplace)
    table.sort(toReplace)

    Msg("[Stools] ") print("Materials after:", #toReplace)
    Msg("[Stools] ") print("Materials other addons have added:", #toReplace - #ourMaterials)

    for i, mat in ipairs(toReplace) do
        matList[i] = mat
    end

    local added = {}
    for i, mat in ipairs(matList) do
        if not _ourMaterials[mat] then
            added[mat:lower()] = true
        end
    end

    concommand.Add("material_supercut_listadded", function()
        for mat in pairs(added) do
            print(mat)
        end
    end)
end)

