    csgo = true,
    hl1 = true,
}

local function checkRealms(str)
    local out = {}

    for realm in pairs(realms) do
        if file.Exists("materials/" .. str .. ".vmt", realm) then
            out[#out + 1] = realm
        end
    end

    return out
end

